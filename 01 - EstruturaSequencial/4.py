#4 - Faça um Programa que peça as 4 notas bimestrais e mostre a média.

b1 = int(input("Digite a nota do 1º Bimestre "))
b2 = int(input("Digite a nota do 2º Bimestre "))
b3 = int(input("Digite a nota do 3º Bimestre "))
b4 = int(input("Digite a nota do 4º Bimestre "))

media = (b1 + b2 + b3 + b4) /4

print(f'A média do aluno é {media}')